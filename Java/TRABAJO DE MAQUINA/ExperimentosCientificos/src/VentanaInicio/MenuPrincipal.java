    public  void precargaCientificos() {
        File f = new File("preCientificos.txt");
        if(f.exists()){
            try{
                FileReader leo = new FileReader(f);
                BufferedReader fLeo = new BufferedReader(leo);
                String linea;
                while((linea = fLeo.readLine())!=null){
                    String [] datos = linea.split("- ");
                    if(datos.length==3){
                        Cientifico c = new Cientifico(datos[1],datos[2],new Fecha());
                        preCientificos.put(Integer.parseInt(datos[0]),c);
                    }
                }
                fLeo.close();
            }catch(IOException e){
                System.out.println(e);
            }
        }else{
            System.out.println("Archivo 'preCientificos.txt' Inexistente");
            System.exit(0);
        }
        
    }
