    public int duracionMayor(){
        int claveMax = 0;
        int duracionMax = Integer.MIN_VALUE;
        for(Integer i : experimentos.keySet()){
            int duracion = 0;
            int diasFin = experimentos.get(i).getFechaFin().pasajeADias();
            int diasInicio = experimentos.get(i).getFechaInicio().pasajeADias();
            duracion= diasFin-diasInicio;
            if(duracion>duracionMax){
                duracionMax = duracion;
                claveMax = i;
            }
        }
        return claveMax;
    }
