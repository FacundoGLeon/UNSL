    private void MODIFICABActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MODIFICABActionPerformed
        
        try{
            if(claveModifica==0){
                verificacionL4.setText("");
                advertenciaL4.setText("SELECCIONA UN EXPERIMENTO");
            }
            else{
                if(descripcionModifica.getText().equals("") || presupuestoModifica.getText().equals("")){
                    verificacionL4.setText("");
                    advertenciaL4.setText("LLENAR TODOS LOS CAMPOS");
                    advertenciaL11.setText("");
                }else{
                    if((float)Double.parseDouble(presupuestoModifica.getText())<=0){
                        advertenciaL11.setText("INGRESE MONTO CORRECTO");
                        verificacionL4.setText("");
                        advertenciaL4.setText("");
                    }else{
                        verificacionL4.setText("MODIFICACION EXITOSA");
                        advertenciaL4.setText("");
                        experimentos.get(claveModifica).setDescripcion(descripcionModifica.getText());
                        experimentos.get(claveModifica).setFechaFin(new Fecha((Integer)dia3B.getSelectedItem(),(Integer)mes3B.getSelectedItem(),(Integer)año3B.getSelectedItem()));
                        experimentos.get(claveModifica).setPresupuesto((float)Double.parseDouble(presupuestoModifica.getText()));
                        cargaCientificos(claveModifica,experimentos.get(claveModifica).getFechaInicio());
                        cargaInstrumentos(claveModifica);
                        cargaTablaModYElim(modeloModif);
                        advertenciaL11.setText("");
                        descripcionModifica.setText("");
                        presupuestoModifica.setText("");
                        año3B.removeAllItems();
                        mes3B.removeAllItems();
                        dia3B.removeAllItems();
                        claveModifica = 0;
                        advertenciaL7.setText("");
                        guardaExperimento();
                    }

                }
            }
        }catch(NumberFormatException e){
            verificacionL4.setText("");
            advertenciaL4.setText("");
            advertenciaL11.setText("INGRESE CORRECTAMENTE");
        }
    }//GEN-LAST:event_MODIFICABActionPerformed

