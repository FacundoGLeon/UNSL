    public void conteoEquiposUtilizados(){
        equipoMasUtilizado.clear();
        for(Integer i : experimentos.keySet()){
            for(Integer j : experimentos.get(i).getInstrumentos().keySet()){
                if(equipoMasUtilizado.containsKey(j)){
                    equipoMasUtilizado.put(j,equipoMasUtilizado.get(j)+1);
                }else{
                    equipoMasUtilizado.put(j, 1);
                }
            }
        }
        equipoMasUtilizado();
    }
    public void equipoMasUtilizado(){
        int valorMasGrande=0;
        Set<Integer> conteo = new HashSet<>();
        
        for(Integer i : equipoMasUtilizado.keySet() ){
            if(equipoMasUtilizado.get(i)>=valorMasGrande){
                valorMasGrande = equipoMasUtilizado.get(i);
            }
        }
        for(Integer i : equipoMasUtilizado.keySet()){
            conteo.add(i);
        }
        
        for(Integer j : conteo){
            if(equipoMasUtilizado.get(j) != valorMasGrande){
                equipoMasUtilizado.remove(j);
            }
        }
    }
