    public void recuperaExperimentos(){
        try{
            File carga = new File("guardaExperimentos.txt");
            if(carga.exists()){
                FileReader lec = new FileReader(carga);
                BufferedReader lectura = new BufferedReader(lec);
                String linea;
                while((linea = lectura.readLine())!=null){
                    String [] datos = linea.split("- ");
                    if(datos.length==12){
                        if(datos[0].equals("Fisico")){
                            experimentos.put(Integer.parseInt(datos[1]), new Fisico(datos[11],datos[2],datos[3],(float)Double.parseDouble(datos[4]),new Fecha(Integer.parseInt(datos[5]),Integer.parseInt(datos[6]),Integer.parseInt(datos[7])),new Fecha(Integer.parseInt(datos[8]),Integer.parseInt(datos[9]),Integer.parseInt(datos[10]))));
                        }else{
                            experimentos.put(Integer.parseInt(datos[1]), new Biologico(datos[11],datos[2],datos[3],(float)Double.parseDouble(datos[4]),new Fecha(Integer.parseInt(datos[5]),Integer.parseInt(datos[6]),Integer.parseInt(datos[7])),new Fecha(Integer.parseInt(datos[8]),Integer.parseInt(datos[9]),Integer.parseInt(datos[10]))));
                        }
                        claveExperimento = Integer.parseInt(datos[1]);
                    }
                }
                recuperaCientificos();
                recuperaInstrumentos();
                lectura.close();
                if(experimentos.isEmpty()){
                    verifica();
                }
                
            }else{
                System.out.println("Archivo 'guardaExperimentos.txt' Inexistente");
                System.exit(0);
            }
            
        }catch(IOException e){
            System.out.println(e);
        }
    }
